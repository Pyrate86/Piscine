int	match(char *s1, char *s2)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (s1[i] != '\0' || s2[c] != '\0')
	{
		while (s2[c] == 42)
		{
			c++;
			while (s2[c] != s1[i] && s2[c] && s1[i] && s2[c] != 42)
				i++;
			if ((s1[i] == '\0' && s2[c] == '\0') || !s2[c])
				return (1);
		}
		if (s1[i] != s2[c])
			return (0);
		i++;
		c++;
	}
	return (1);
}
